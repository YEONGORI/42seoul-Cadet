char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i] != 0)
		if (s[i] == c)
			return ((char *)(s + i));
	if (s[i] == 0 && c == '\0')
		return ((char *)(s + i));
	return (0);
}
